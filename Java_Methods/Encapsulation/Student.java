public class Student { 
	private String name;
	private int age;

	// Setter
	public void setName(String newName){
		name = newName;

	}

	// Getter
	public String getName(){
		return name;
	}

	// Setter
	public void setAge(int newAge) {
		age = newAge;
	}

	// Getter
	public int geAge() {
		return age;
	}

}
